       'String', 'Manifold Embedding Neuroimage Analysis (MENA)',...
       'BackgroundColor',bgColor,...
       'FontSize',fontSize,...
       'HorizontalAlignment','left',...
       'Position', [round(0.3*width) height-50-(fontSize*1.5) ...
                    round(0.65*width) fontSize*2]);

