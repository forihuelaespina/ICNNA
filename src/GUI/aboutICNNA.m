       'String', 'Imperial College Near Infrared Spectroscopy Neuroimaging Analysis (ICNNA)',...
       'BackgroundColor',bgColor,...
       'FontSize',fontSize,...
       'FontWeight','bold',...
       'HorizontalAlignment','left',...
       'Position', [round(0.3*width) height-30-(fontSize*1.5) ...
                    round(0.65*width) fontSize*4]);
                
                

