    'Function cca => (c) SOM Toolbox',...
    'Function kldiv => (c) David Fass',...
    'Functions kmeans, dist2 => (c) netlab. Ian T Nabney (1996-2001)',...
    'Isomap algorithm => (c) Tenenbaum',...
    ''};
    
