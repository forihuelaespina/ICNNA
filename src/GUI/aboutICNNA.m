    'Dr. Dave James, Prof. Sir Ara Darzi and Prof. Guang-Zhong Yang'],...
    '', ...
    ['Beta testing: Dr. Felipe Orihuela-Espina, Mr. Marco Caproni, ' ...
    'Dr. Dave James, Dr. Kunal Shetty and Dr. Samuel A. Montero-Hernández, ' ...
    'Dr. Harsimrat Singh, Mrs. Michele Rojas Cisneros.'],...
    '', ...
    '=================================',...
    '== External sources code and algorithms',...
    '=================================',...
    ['In some cases, ICNNA uses code not only from basic MATLAB, ' ...
    'software package ' ...
    'but also from other sources but ditributed under ' ...
    'licences. These are in some cases ' ...
    'copyright protected, and their rights belong to ' ...
    'their authors.'],...
