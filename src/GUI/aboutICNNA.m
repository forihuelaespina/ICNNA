       'String', 'Author: Felipe Orihuela-Espina',...
       'BackgroundColor',bgColor,...
       'FontSize',fontSize,...
       'HorizontalAlignment','left',...
       'Position', [round(0.3*width) height-80-(fontSize*1.5) ...
                    round(0.65*width) fontSize*2]);

uicontrol(f,'Style', 'text',...
       'String', '(c) 2008-19 Felipe Orihuela-Espina. Imperial College',...
       'BackgroundColor',bgColor,...
       'FontSize',fontSize,...
       'HorizontalAlignment','left',...
       'Position', [round(0.3*width) height-110-(fontSize*1.5) ...
                    round(0.65*width) fontSize*2]);


                
A=imread('./GUI/icons/ICNNA_jack.jpg','jpeg');
image(A);
set(gca,'Tag','logoAxes',...
       'Color','w',...
       'FontSize',fontSize,...
       'Units','normalize',...
       'Position', [0.02 0.73 0.18 0.25]);
       %'Position', [0.02 0.58 0.25 0.35]);
